<template>
  <div
    class="mapgis-ui-borderbox7"
    :style="`box-shadow: inset 0 0 40px ${mergedColor[0]}; border: 1px solid ${mergedColor[0]}; background-color: ${backgroundColor}`"
    :ref="ref"
  >
    <svg class="mapgis-ui-border-svg-container" :width="width" :height="height">
      <polyline
        class="mapgis-ui-bb7-line-width-2"
        :stroke="mergedColor[0]"
        :points="`0, 25 0, 0 25, 0`"
      />
      <polyline
        class="mapgis-ui-bb7-line-width-2"
        :stroke="mergedColor[0]"
        :points="`${width - 25}, 0 ${width}, 0 ${width}, 25`"
      />
      <polyline
        class="mapgis-ui-bb7-line-width-2"
        :stroke="mergedColor[0]"
        :points="`${width - 25}, ${height} ${width}, ${height} ${width}, ${
          height - 25
        }`"
      />
      <polyline
        class="mapgis-ui-bb7-line-width-2"
        :stroke="mergedColor[0]"
        :points="`0, ${height - 25} 0, ${height} 25, ${height}`"
      />

      <polyline
        class="mapgis-ui-bb7-line-width-5"
        :stroke="mergedColor[1]"
        :points="`0, 10 0, 0 10, 0`"
      />
      <polyline
        class="mapgis-ui-bb7-line-width-5"
        :stroke="mergedColor[1]"
        :points="`${width - 10}, 0 ${width}, 0 ${width}, 10`"
      />
      <polyline
        class="mapgis-ui-bb7-line-width-5"
        :stroke="mergedColor[1]"
        :points="`${width - 10}, ${height} ${width}, ${height} ${width}, ${
          height - 10
        }`"
      />
      <polyline
        class="mapgis-ui-bb7-line-width-5"
        :stroke="mergedColor[1]"
        :points="`0, ${height - 10} 0, ${height} 10, ${height}`"
      />
    </svg>

    <div class="border-box-content">
      <slot></slot>
    </div>
  </div>
</template>

<script>
import autoResize from "../../mixin/AutoResize";
import clonedeep from "lodash.clonedeep";

export default {
  name: "mapgis-ui-borderbox7",
  mixins: [autoResize],
  props: {
    color: {
      type: Array,
      default: () => ([])
    },
    backgroundColor: {
      type: String,
      default: 'transparent'
    }
  },
  data() {
    return {
      ref: "border-box-7",
      defaultColor: ['rgba(128,128,128,0.3)', 'rgba(128,128,128,0.5)'],
      mergedColor: []
    };
  },
  watch: {
    color() {
      const { mergeColor } = this;

      mergeColor();
    },
  },
  methods: {
    mergeColor() {
      const { color, defaultColor } = this;
      let colors = color || [];
      this.mergedColor = colors.concat(clonedeep(defaultColor));
    },
  },
  mounted() {
    const { mergeColor } = this;

    mergeColor();
  },
};
</script>
